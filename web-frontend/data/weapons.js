// Ammo count: 0=none, 1-8=count, 9=infinite
export function createDefaultWeaponSet(name) {
  const ammo = {};
  const delay = {};
  const prob = {};
  const crate = {};
  
  for (const w of WEAPONS) {
    ammo[w.id] = 2;
    delay[w.id] = 0;
    prob[w.id] = 0;
    crate[w.id] = 1;
  }
  
  return { name, ammo, delay, prob, crate };
}

